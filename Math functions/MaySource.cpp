#include<cmath>


// реализация функций

int sum(int a, int b) // сложение
{
	return a + b;
}
double minus(double a, double b) // вычитание
{
	return a - b;
}
double multi(double a, double b) // умножение
{
	return  a * b;
}
double divide(double a, double b) // деление
{
	return a / b;
}
double expo(double a, double b) // степень
{
	return pow(a, b);
}